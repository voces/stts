export const getFrameName = (frame: FrameEx) => {
  if (!seeded) {
    seeded = true;
    for (const [name, context] of frameNames) knownFrames[`${name}_${context}`] = FrameEx.fromName(name, context);
  }
  for (const key in knownFrames) {
    if (frame.handle === knownFrames[key].handle) return key;
  }
  return "unknown";
};
